declare let process: {
    env: {
        NODE_ENV: string
        VUE_APP_API_ENDPOINT: string
        VUE_APP_API_SUBSCRIPTIONS_ENDPOINT: string
        VUE_APP_SENTRY_SECURITY_TOKEN: string
        VUE_APP_ETH_NETWORK: string
    }
}

export class ConfigHelper {
    public static get isRopsten(): boolean {
        return process.env.VUE_APP_ETH_NETWORK === 'ropsten'
    }
}
